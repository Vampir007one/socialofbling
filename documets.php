                    23.10. Рассматривает состояние реабилитационной работы, рационального трудоустройства инвалидов по зрению, социокультурной, физкультурно-оздоровительной работы и спорта, работы со слепоглухими, других вопросов деятельности РО ВОС и определяет меры по их улучшению.
        
