        Статья 22
        
        Высшим руководящим органом региональной организации ВОС является конференция.
        
        Конференция РО ВОС созывается по решению правления региональной организации ВОС один раз в пять лет. Срок и проект повестки дня конференции объявляются не позднее, чем за два месяца до ее проведения.
        
        Норму представительства на конференцию определяет правление РО ВОС в соответствии с рекомендациями ЦП ВОС.
        
        Внеочередная конференция РО ВОС может быть созвана по решению Центрального правления ВОС, инициативе правления региональной организации, или по требованию более половины местных организаций региональной организации ВОС.
        
        Конференция региональной организации правомочна, при участии в ее работе более половины избранных делегатов.
        
        Конференция региональной организации ВОС:
        
