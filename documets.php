    <p>Устав Общероссийской общественной организации инвалидов «Всероссийское ордена Трудового Красного Знамени общество слепых»

        Раздел 1. ОБЩИЕ ПОЛОЖЕНИЯ
        
        Статья 1
        
        Общероссийская общественная организация инвалидов «Всероссийское ордена Трудового Красного Знамени общество слепых» - организация, основанная на добровольном членстве граждан РФ - инвалидов по зрению, их законных представителей, других граждан, активно принимающих участие в жизни организации, и созданная для защиты прав и интересов инвалидов по зрению, их социальной поддержки, реабилитации, социальной интеграции и содействия обеспечению равных возможностей (далее ВОС).
        
        Полное наименование организации: Общероссийская общественная организация инвалидов «Всероссийское ордена Трудового Красного Знамени общество слепых».
        
        Сокращенное наименование организации - ВОС.
        
        ВОС осуществляет свою деятельность на всей территории Российской Федерации и действует в соответствии с Конституцией Российской Федерации, Гражданским кодексом Российской Федерации, Федеральными законами «О некоммерческих организациях», «Об общественных объединениях», « О социальной защите инвалидов в Российской Федерации», иными законодательными актами Российской Федерации о социальной защите населения и настоящим Уставом.
        
        ВОС от своего имени приобретает и осуществляет имущественные и личные неимущественные права, отвечает по своим обязательствам находящимся в его собственности имуществом, является истцом и ответчиком в суде.
        
        ВОС имеет гимн, флаг, круглую печать, угловой штамп, бланки со своим наименованием, собственную символику и другие реквизиты. Описание и порядок использования гимна, флага и символики устанавливаются постоянно действующим руководящим органом ВОС.
        
        ВОС учреждает свои награды, почетные звания, знаки отличия, грамоты для членов ВОС и других лиц, активно участвующих в жизни Общества.
        С момента государственной регистрации ВОС приобретает права юридического лица.
        
        Место нахождения постоянно действующего руководящего органа Всероссийского общества слепых - город Москва, Российская Федерация.
        
        Статья 2
        
        Деятельность ВОС основывается на принципах:
        
                    2.1. Гуманизма и милосердия, уважения личного достоинства и мнения каждого члена ВОС, недопущения дискриминации инвалидов по признакам возраста, национальности, религиозным и политическим убеждениям, причинам инвалидности.
        
        2.2. Добровольности, равноправия, самоуправления и законности, выборности всех руководящих и контрольно-ревизионных органов ВОС снизу доверху, коллегиальности и гласности в их работе, периодической отчетности перед вышестоящими руководящими органами и членами ВОС.
        
        2.3. Обязательности выполнения решений вышестоящих руководящих органов ВОС для нижестоящих в пределах их компетенции.
        
        2.4. Самостоятельности структурных подразделений ВОС в их деятельности в соответствии с Уставом ВОС и другими нормативными актами ВОС.
        
        2.5. Общности интересов членов ВОС и их личной ответственности за выполнение уставных требований. 
        
         
        
        Раздел 2. ЦЕЛИ, ЗАДАЧИ, ПРЕДМЕТ ДЕЯТЕЛЬНОСТИ, ПРАВА И   ОБЯЗАННОСТИ ВОС
        
        Статья 3
        
        Основными целями ВОС являются:
        
        участие в определении и реализации государственной политики в отношении инвалидов, содействие и участие в реализации государственных полномочий в медицинской, профессиональной, социальной реабилитации и интеграции, приобщении к труду, образованию, культуре и спорту, улучшении их материально-бытовых условий, участие в законодательной деятельности и защита в исполнительных, судебных органах государственной власти Российской Федерации, субъектов Российской Федерации и в органах местного самоуправления прав и законных интересов инвалидов по зрению как потребителей товаров, работ и услуг, предназначенных для их социальной, медицинской и профессиональной реабилитации.
        
        Статья 4
        
        Для достижения указанных целей ВОС решает следующие основные задачи и осуществляет следующие направления и виды деятельности:
        
        4.1. Выявляет инвалидов по зрению, ведет учет и вовлекает их в члены ВОС. Привлекает зрячих граждан к работе в Обществе для обеспечения выполнения уставных задач ВОС.
        
        4.2. Участвует в осуществлении мероприятий по социальной защите и интеграции инвалидов по зрению. Совместно с органами государственной власти РФ и субъектов РФ, органами местного самоуправления создает инвалидам по зрению необходимые условия для реализации гражданских и социально-экономических прав и возможностей. Участвует в формировании доступной среды жизнедеятельности и обеспечении беспрепятственного доступа инвалидов по зрению к объектам социальной сферы.
        
        4.3. Совместно с органами государственной власти РФ и субъектов РФ, органами местного самоуправления участвует в разработке проектов законодательных и иных нормативных актов по решению проблем инвалидов и их реализации, разработке и реализации программ реабилитации и социальной поддержке инвалидов.
        
        4.4. Взаимодействует с федеральными учреждениями медико–социальной экспертизы, содействует медицинской, профессиональной и социальной реабилитации инвалидов по зрению, способствует разработке и реализации индивидуальных программ реабилитации инвалида (ИПР).
        
        4.5. Взаимодействует с органами здравоохранения по вопросам улучшения медицинской помощи инвалидам по зрению и проведения работы по профилактике слепоты, сохранению остаточного зрения, а также осязания, слуха и здоровья инвалидов по зрению в целом.
        
        4.6. Осуществляет издательскую, рекламную и иную информационную деятельность в Российской Федерации и за ее пределами.
        
        4.7. Оказывает органам службы занятости всех уровней содействие в трудоустройстве инвалидов по зрению в организациях всех форм собственности. Организует трудоустройство инвалидов по зрению в хозяйственных обществах, организациях и учреждениях ВОС, содействует их индивидуально-трудовой деятельности.
        
        4.8. Осуществляет производственное и профессиональное обучение, повышение квалификации инвалидов по зрению, работающих в системе ВОС, в соответствии с действующим законодательством.
        
        4.9. Способствует повышению образовательного уровня инвалидов по зрению и обучению чтению и письму по Брайлю, пользованию компьютерной техникой и современными информационными технологиями. Шефствует над учащимися слепыми и слабовидящими детьми школ, школ-интернатов и иных общеобразовательных учреждений. Содействует открытию классов и школ-интернатов для слепых и слабовидящих детей. В целях содействия социальной реабилитации и профессиональной ориентации, участвует в создании детских и молодежных объединений, оказывает семьям, в которых родители и/или дети инвалиды с нарушениями зрения, помощь по семейной реабилитации и их интеграции в общество.
        
        4.10. Способствует реализации мероприятий по улучшению бытового обслуживания и жилищных условий членов Общества.
        
        4.11. Участвует в разработке конструкций технических приборов и специальных приспособлений, облегчающих труд инвалидов по зрению, расширяющих сферу его применения, улучшающих бытовые условия, ориентировку слепых в пространстве и информационное обеспечение, используя для этого возможности Общества, а также министерств, ведомств, научных и иных государственных и негосударственных организаций независимо от форм собственности.
        
        4.12. Принимает меры для создания условий развития личности и удовлетворения духовных запросов инвалидов по зрению, содействует их нравственно-эстетическому воспитанию, экономическому просвещению.
        
        4.13. Совместно с органами государственной власти РФ и субъектов РФ, органами местного самоуправления осуществляет реабилитацию средствами физической культуры и спорта, а также социокультурную реабилитацию инвалидов по зрению, проводит физкультурно-оздоровительные и спортивные мероприятия, развивает среди них социальный туризм.
        
        4.14. Осуществляет деятельность, приносящую доходы, направляемые на выполнение уставных задач Общества.
        
        4.15. Сотрудничает с другими общественными объединениями инвалидов с целью координации действий по защите прав и интересов инвалидов, их социальной поддержки, а также в решении проблем организаций инвалидов.
        
        4.16. Осуществляет обмен опытом работы в области социальной защиты инвалидов по зрению с другими общественными объединениями РФ, а также зарубежными, объединенными Всемирным и Европейским союзами слепых, другими международными организациями.
        
        4.17. Организует правовое просвещение инвалидов по зрению, при необходимости оказывает им консультационную юридическую помощь.
        
        Статья 5
        
        Для осуществления уставных задач ВОС имеет право:
        
        5.1. Свободно распространять информацию, популяризировать свою деятельность и возможности инвалидов по зрению.
        
        5.2. Участвовать в выработке решений органов государственной власти и органов местного самоуправления по проблемам, касающимся интересов инвалидов по зрению, в порядке и объеме, предусмотренными законодательством РФ.
        
        5.3. Выступать с инициативами и вносить предложения в органы государственной власти и органы местного самоуправления по различным вопросам жизни и деятельности инвалидов по зрению.
        
        5.4. Участвовать в избирательных кампаниях и референдумах в соответствии с федеральными законами и законами субъектов Российской Федерации.
        
        5.5. Представлять и защищать свои права, а также законные интересы инвалидов по зрению и других членов ВОС в органах государственной власти, органах местного самоуправления, судебных органах и иных организациях.
        
        5.6. Самостоятельно и совместно с другими общественными организациями создавать различные фонды, союзы, ассоциации, другие общественные объединения и взаимодействовать с существующими.
        
        5.7. Учреждать средства массовой информации, осуществлять издательскую деятельность, выпускать для инвалидов по зрению специальную литературу по реабилитации, а также периодическую, научно-техническую и художественную.
        
        5.8. Устанавливать взаимосвязи с благотворительными фондами и иными благотворительными организациями, организовывать работу по привлечению бюджетных средств и добровольных пожертвований.
        
        5.9. Вступать в международные общественные объединения, приобретать права и выполнять обязанности, соответствующие статусу этих международных общественных объединений.
        
        5.10. Поддерживать прямые международные контакты и связи, заключать соглашения с иностранными некоммерческими неправительственными объединениями, различными фондами и другими организациями.
        
        5.11. Создавать свои организации, отделения или филиалы и представительства в иностранных государствах на основе общепризнанных принципов и норм международного права, международных договоров Российской Федерации и законодательств этих государств.
        
        5.12. Самостоятельно или совместно с медицинскими, образовательными, культурно-просветительными и научно-исследовательскими организациями, организациями, оказывающими социальные услуги, с органами социальной защиты населения давать заключения о пригодности товаров, работ и услуг, предназначенных для социальной, медицинской и профессиональной реабилитации инвалидов по зрению.
        
        5.13. Для осуществления деятельности, приносящей доходы, создавать хозяйственные общества и иные коммерческие организации в соответствии с Гражданским кодексом РФ.
        
        5.14. Для выполнения управленческих, социально-культурных и иных функций некоммерческого характера создавать учреждения.
        
        5.15. Для пополнения средств на осуществление различных социальных программ создавать различные фонды.
        
                    5.16. Осуществлять в полном объеме иные права, предусмотренные Федеральными законами «О некоммерческих организациях», «Об общественных объединениях», «О социальной защите инвалидов в Российской Федерации» и другими законами, нормативными правовыми актами Российской Федерации, субъектов Российской Федерации, нормативными правовыми актами муниципальных образований в сфере социальной защиты инвалидов по зрению, защите прав и законных интересов инвалидов по зрению, групп инвалидов по зрению, неопределенного круга инвалидов по зрению, как потребителей товаров, работ и услуг, предназначенных для их социальной, медицинской и профессиональной реабилитации.
        
        Статья 6
        
        ВОС обязан:
        
        6.1. Соблюдать законодательство Российской Федерации, общепризнанные принципы и нормы международного права, касающиеся сферы деятельности ВОС, а также нормы, предусмотренные Уставом ВОС.
        
        6.2. Ежегодно публиковать отчет об экономическом состоянии Общества и исполнении бюджета ВОС.
        
        6.3. Ежегодно информировать орган, принявший решение о государственной регистрации ВОС, о продолжении своей деятельности с указанием действительного места нахождения постоянно действующего руководящего органа ВОС, его названия и данных о руководителях ВОС в объеме сведений, включаемых в единый государственный реестр юридических лиц.
        
        6.4. Представлять по запросу органа, принимающего решение о государственной регистрации ВОС, соответствующие документы руководящих органов и должностных лиц ВОС, а также годовые и квартальные отчеты о своей деятельности в объеме сведений, представляемых в налоговые органы.
        
                             
        
           Раздел 3. СТРУКТУРА ВОС
        
        Статья 7
        
        ВОС, как общероссийская общественная организация, действует на всей территории Российской Федерации, самостоятельно определяет свои постоянно действующие руководящие органы, внутреннюю структуру, цели, формы и методы деятельности в соответствии с действующим законодательством РФ.
        
        Структуру ВОС образуют его региональные организации (отделения), местные организации (филиалы региональных отделений).
        
        Статья 8
        
        Для осуществления уставных задач в субъектах Российской Федерации решением постоянно действующего руководящего органа ВОС - Центрального правления ВОС (далее ЦП ВОС), создаются республиканские, краевые, областные, Московская городская и Санкт-Петербургская, объединяющая инвалидов по зрению, проживающих в г. Санкт-Петербурге и Ленинградской области, региональные организации (отделения) ВОС.
        
        Статья 9
        
        Региональные организации ВОС (далее РО ВОС) действуют на основании единого устава - Устава ВОС и других нормативных документов ВОС, подлежат государственной регистрации в соответствии с действующим законодательством и после регистрации являются юридическими лицами.
        
        Статья 10
        
        ВОС, в лице президента ВОС, и региональные организации, в лице их председателей, представляют ВОС в органах государственной власти РФ и субъектов РФ, органах местного самоуправления и в других организациях без доверенности.
        
        Статья 11
        
        В региональных организациях ВОС по решению правления, согласованному с ЦП ВОС, создаются местные организации при наличии не менее 15 членов Общества – инвалидов по зрению.
        
        Местная организация ВОС не является юридическим лицом.
        
        Решением бюро в местных организациях могут создаваться группы в составе не менее трех членов ВОС.
        
        Статья 12
        
        Права, обязанности и задачи местных организаций ВОС определяются настоящим Уставом и другими нормативными актами ВОС.
        
        Раздел 4. ЧЛЕНСТВО В ВОС
        
        Статья 13
        
        Членами ВОС могут быть граждане РФ, достигшие 18-летнего возраста и являющиеся инвалидами по зрению, их законными представителями (один из родителей, усыновителей, опекун или попечитель), разделяющие цели и задачи Общества и признающие Устав ВОС.
        
        В члены ВОС могут быть приняты и другие граждане, признающие Устав ВОС и активно содействующие решению проблем Общества.
        
        Вопрос о приеме в члены ВОС граждан РФ по их письменным заявлениям решает бюро местных организаций большинством голосов.
        
        Статья 14
        
        Член ВОС имеет право:
        
        14.1. Участвовать в обсуждении и решении всех вопросов деятельности Общества.
        
        14.2. Избирать и быть избранным в выборные руководящие и контрольно-ревизионные органы Всероссийского общества слепых.
        
        14.3. Обжаловать решения руководящих органов и должностных лиц ВОС в вышестоящий руководящий орган ВОС.
        
        14.4. Пользоваться поддержкой и защитой ВОС.
        
        Статья 15
        
        Член ВОС обязан:
        
        15.1. Выполнять требования Устава ВОС.
        
        15.2. Состоять на учете и принимать участие в работе одной из местных организаций ВОС. Выполнять решения руководящих органов ВОС.
        
        15.3. Уплачивать членские взносы.
        
        15.4. Уважать права и законные интересы членов ВОС и своей организации.
        
        15.5. Поддерживать авторитет и единство Общества.
        
        15.6. Не наносить ущерб ВОС в любой форме и не совершать действий, дискредитирующих Общество.
        
        Статья 16
        
        В ВОС действует единый членский билет ВОС. Образец членского билета утверждается ЦП ВОС.
        
        Статья 17
        
        Членство в Обществе прекращается по письменному заявлению члена ВОС о добровольном выходе из него.
        
        За невыполнение Устава ВОС, потерю связи с Обществом, совершение действий, нанесших моральный или материальный ущерб ВОС и порочащих его репутацию, распространение заведомо ложной информации о деятельности ВОС, его выборных органов и должностных лиц, утрату доверия его членов, а также неуплату членских взносов в течение 2-х лет член ВОС может быть исключен из членов Общества.
        
        Исключение из членов ВОС осуществляется решением бюро местной организации , принятым большинством голосов.
        
        За исключенным из членов ВОС остается право в двухмесячный срок подать апелляцию в вышестоящий руководящий орган ВОС.
        
        Статья 18
        
        По ходатайству ВОС перед Президентом РФ члены ВОС, имеющие особые заслуги перед Обществом, могут быть представлены к государственным наградам и присвоению почетных званий.
        
        Ветераны ВОС и лица, активно участвующие в жизни Общества, решениями съезда ВОС или Центрального правления ВОС могут быть награждены наградами ВОС и им могут присваиваться почетные звания ВОС.
        
        В региональных организациях ВОС могут устанавливаться другие виды поощрения и отличия членов ВОС.
        
        За нарушение уставных обязанностей, совершение противоправных действий награжденные наградами ВОС, решением соответствующих руководящих органов Общества, могут быть лишены полученных ранее званий и наград Всероссийского общества слепых.
        
        
        Раздел 5. РУКОВОДЯЩИЕ ОРГАНЫ ВОС
        
        Статья 19
        
