        Кандидатура председателя РО ВОС может выдвигаться и делегатами конференции.
        
        Избирая председателя РО ВОС, одновременно избирает его председателем правления РО ВОС.
        
        22.4. Избирает по предложению председателя региональной организации правление региональной организации ВОС.
        
        22.5. Избирает делегатов на съезд ВОС.
        
        22.6. Избирает и досрочно прекращает полномочия председателя КРК РО ВОС.
        
